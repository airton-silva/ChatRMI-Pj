     public void displayChatList() throws RemoteException {
        chatList.setText("");
        msgs = chat.getAllMessages();
        String doc = ">>> |";
        for (Message m : msgs) {
            if (!((m.getUsername().equals(username)) && (m.getType().equals("juntar")))) {             
                String existMensagem = null;
                String msg = m.getMsg();
                Date date = m.getDate();
                DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                String a = dateFormat.format(date);
                
                SimpleDateFormat parseFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                SimpleDateFormat printFormat = new SimpleDateFormat("HH:mm:ss");
                
                try {
                    Date time = parseFormat.parse(a);
                    if (existMensagem == null) {                        
                        doc += " "+ printFormat.format(time)+"\t"
                                + m.getUsername()+ "\t"
                                + m.getMsg()+"\n";

                   }
                } catch (ParseException ex) {
                    Logger.getLogger(ChatView.class.getName()).log(Level.SEVERE, null, ex);
                }                        
            }
        }
        doc += ">>>";
        chatList.setText(doc);
    }
    
    private void displayUserList() throws RemoteException {
        userList.setText("");
        users = chat.getAllUsers();
        for (String u : users) {
            if (!u.equals(username)) {             
                userList.append(" " + u + "\n");             
            }
        }
    }
    
    public void refresh() {
        try {
            displayChatList();
            displayUserList();
        } catch (RemoteException ex) {
            Logger.getLogger(ChatView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
